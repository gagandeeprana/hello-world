    private void btnSaveAddCompanyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAddCompanyActionPerformed
        String msg = "";
        if (addUpdateFlag.equals("update")) {
            companyBean = new CompanyBean();
            companyBean.setCompanyId(Integer.parseInt(txtCompanyIdAddCompany.getText()));
            companyBean.setCompanyName(txtCompanyNameAddCompany.getText());
            msg = companyUI.update(companyBean);
        } else {
            msg = companyUI.save();
        }
        JOptionPane.showMessageDialog(null, msg);
        dispose();
    }//GEN-LAST:event_btnSaveAddCompanyActionPerformed

    private void txtCompanyIdAddCompanyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCompanyIdAddCompanyKeyReleased
        CompanyDAO companyDAO = new CompanyDAOImpl();
        List<CompanyBean> lstCompanies = companyDAO.getAllCompanies("");
        try {
            if (validateCompanyId(txtCompanyIdAddCompany.getText())) {
                lblCompanyIdAddCompanyFrameValidation.setText("");
                for (CompanyBean companyBean : lstCompanies) {
                    if (companyBean.getCompanyId() == Integer.parseInt(txtCompanyIdAddCompany.getText().trim())) {
                        lblCompanyIdAddCompanyFrameValidation.setText("Already Taken");
                        btnSaveAddCompany.setEnabled(false);
                    }
                }
            } else {
                lblCompanyIdAddCompanyFrameValidation.setText(msg);
                btnSaveAddCompany.setEnabled(false);
            }
            if (lblCompanyIdAddCompanyFrameValidation.getText().equals("") && lblCompanyNameAddCompanyFrameValidation.getText().equals("")) {
                btnSaveAddCompany.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txtCompanyIdAddCompanyKeyReleased

    private void txtCompanyNameAddCompanyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCompanyNameAddCompanyKeyReleased
        CompanyDAO companyDAO = new CompanyDAOImpl();
        List<CompanyBean> lstCompanies = companyDAO.getAllCompanies("");
        try {
            if (validateCompanyName(txtCompanyNameAddCompany.getText())) {
                lblCompanyNameAddCompanyFrameValidation.setText("");
                for (CompanyBean companyBean : lstCompanies) {
                    if (companyBean.getCompanyName().equals(txtCompanyNameAddCompany.getText().trim())) {
                        lblCompanyNameAddCompanyFrameValidation.setText("Already Taken");
                        btnSaveAddCompany.setEnabled(false);
                    }
                }
            } else {
                lblCompanyNameAddCompanyFrameValidation.setText(msg);
                btnSaveAddCompany.setEnabled(false);
            }
            if (lblCompanyIdAddCompanyFrameValidation.getText().equals("") && lblCompanyNameAddCompanyFrameValidation.getText().equals("")) {
                btnSaveAddCompany.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txtCompanyNameAddCompanyKeyReleased

