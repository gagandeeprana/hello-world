    private void btnSaveAddRoleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAddRoleActionPerformed
        String msg = "";
        if (addUpdateFlag.equals("update")) {
            roleBean = new RoleBean();
            roleBean.setRoleId(Integer.parseInt(txtRoleIdAddRole.getText()));
            roleBean.setRoleName(txtRoleNameAddRole.getText());
            msg = roleUI.update(roleBean);
        } else {
            msg = roleUI.save();
        }
        JOptionPane.showMessageDialog(null, msg);
        dispose();
    }//GEN-LAST:event_btnSaveAddRoleActionPerformed

    private void txtRoleIdAddRoleKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRoleIdAddRoleKeyReleased
        RoleDAO roleDAO = new RoleDAOImpl();
        List<RoleBean> lstRoles = roleDAO.getAllRoles("");
        try {
            if (validateRoleId(txtRoleIdAddRole.getText())) {
                lblRoleIdAddRoleFrameValidation.setText("");
                for (RoleBean roleBean : lstRoles) {
                    if (roleBean.getRoleId() == Integer.parseInt(txtRoleIdAddRole.getText().trim())) {
                        lblRoleIdAddRoleFrameValidation.setText("Already Taken");
                        btnSaveAddRole.setEnabled(false);
                    }
                }
            } else {
                lblRoleIdAddRoleFrameValidation.setText(msg);
                btnSaveAddRole.setEnabled(false);
            }
            if (lblRoleIdAddRoleFrameValidation.getText().equals("") && lblRoleNameAddRoleFrameValidation.getText().equals("")) {
                btnSaveAddRole.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txtRoleIdAddRoleKeyReleased

    private void txtRoleNameAddRoleKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRoleNameAddRoleKeyReleased
        RoleDAO roleDAO = new RoleDAOImpl();
        List<RoleBean> lstRoles = roleDAO.getAllRoles("");
        try {
            if (validateRoleName(txtRoleNameAddRole.getText())) {
                lblRoleNameAddRoleFrameValidation.setText("");
                for (RoleBean roleBean : lstRoles) {
                    if (roleBean.getRoleName().equals(txtRoleNameAddRole.getText().trim())) {
                        lblRoleNameAddRoleFrameValidation.setText("Already Taken");
                        btnSaveAddRole.setEnabled(false);
                    }
                }
            } else {
                lblRoleNameAddRoleFrameValidation.setText(msg);
                btnSaveAddRole.setEnabled(false);
            }
            if (lblRoleIdAddRoleFrameValidation.getText().equals("") && lblRoleNameAddRoleFrameValidation.getText().equals("")) {
                btnSaveAddRole.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txtRoleNameAddRoleKeyReleased

