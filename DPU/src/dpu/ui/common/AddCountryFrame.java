    private void btnSaveAddCountryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAddCountryActionPerformed
        String msg = "";
        if (addUpdateFlag.equals("update")) {
            countryBean = new CountryBean();
            countryBean.setCountryId(Integer.parseInt(txtCountryIdAddCountry.getText()));
            countryBean.setCountryName(txtCountryNameAddCountry.getText());
            msg = countryUI.update(countryBean);
        } else {
            msg = countryUI.save();
        }
        JOptionPane.showMessageDialog(null, msg);
        dispose();
    }//GEN-LAST:event_btnSaveAddCountryActionPerformed

    private void txtCountryIdAddCountryKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCountryIdAddCountryKeyReleased
        CountryDAO countryDAO = new CountryDAOImpl();
        List<CountryBean> lstCompanies = countryDAO.getAllCountries("");
        try {
            if (validateCountryId(txtCountryIdAddCountry.getText())) {
                lblCountryIdAddCountryFrameValidation.setText("");
                for (CountryBean countryBean : lstCompanies) {
                    if (countryBean.getCountryId() == Integer.parseInt(txtCountryIdAddCountry.getText().trim())) {
                        lblCountryIdAddCountryFrameValidation.setText("Already Taken");
                        btnSaveAddCountry.setEnabled(false);
                    }
                }
            } else {
                lblCountryIdAddCountryFrameValidation.setText(msg);
                btnSaveAddCountry.setEnabled(false);
            }
            if (lblCountryIdAddCountryFrameValidation.getText().equals("") && lblCountryNameAddCountryFrameValidation.getText().equals("")) {
                btnSaveAddCountry.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txtCountryIdAddCountryKeyReleased

    private void txtCountryNameAddCountryKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCountryNameAddCountryKeyReleased
        CountryDAO countryDAO = new CountryDAOImpl();
        List<CountryBean> lstCountries = countryDAO.getAllCountries("");
        try {
            if (validateCountryName(txtCountryNameAddCountry.getText())) {
                lblCountryNameAddCountryFrameValidation.setText("");
                for (CountryBean countryBean : lstCountries) {
                    if (countryBean.getCountryName().equals(txtCountryNameAddCountry.getText().trim())) {
                        lblCountryNameAddCountryFrameValidation.setText("Already Taken");
                        btnSaveAddCountry.setEnabled(false);
                    }
                }
            } else {
                lblCountryNameAddCountryFrameValidation.setText(msg);
                btnSaveAddCountry.setEnabled(false);
            }
            if (lblCountryIdAddCountryFrameValidation.getText().equals("") && lblCountryNameAddCountryFrameValidation.getText().equals("")) {
                btnSaveAddCountry.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_txtCountryNameAddCountryKeyReleased

